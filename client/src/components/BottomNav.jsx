export default function BottomNav() {
  const { user } = useAuth();
  const [newCount, setNewCount] = useState(0);
  const items = navItems[user?.role] || [];

  useEffect(() => {
    if (user?.role !== 'supervisor') return;
    const poll = () => api.getNewLeadCount().then(d => setNewCount(d.count)).catch(() => {});
    poll();
    const interval = setInterval(poll, 30000);
    return () => clearInterval(interval);
  }, [user?.role]);

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-white border-t border-gray-200 z-50 shadow-[0_-2px_10px_rgba(0,0,0,0.06)]">
      <div className="flex justify-around max-w-lg mx-auto">
        {items.map(item => (
          <NavLink
            key={item.to}
            to={item.to}
            className={({ isActive }) =>
              `flex flex-col items-center py-2.5 px-4 text-[11px] font-medium transition-colors ${
                isActive ? 'text-brand-900' : 'text-gray-400'
              }`
            }
          >
            <span className="relative mb-0.5">
              {item.icon}
              {item.badge && newCount > 0 && (
                <span className="absolute -top-1.5 -right-2.5 bg-red-500 text-white text-[9px] font-bold rounded-full min-w-[18px] h-[18px] flex items-center justify-center px-1 shadow-sm">
                  {newCount > 99 ? '99+' : newCount}
                </span>
              )}
            </span>
            <span>{item.label}</span>
          </NavLink>
        ))}
      </div>
    </nav>
  );
}
